#include <stdio.h> // 우선 { } 의 역할은 중괄호 { } 안의 코드들을 묶어주는 역할을 한다.
void main() // void는 메모리를 차지하지 않기 때문에 void 타입의 변수는 성언이 불가, 중괄호 '{'로 시작하여 '}'로 끝난다.
{
	double a, b, c; // 자료형을 선언한 것과 마찬가지인 결과가 나타납니다

	a = 3.14;// 소수점 이하 16자리까지 표현할 수 있다.
	b = 0.82;// 소수점 이하 16자리까지 표현할 수 있다.

	c = a * b; //a와 b의 값을 곱한 결과를 c에 저장

	printf("%f 곱하기 %f dms %f 입니다/n", a, b, c); // 3.140000 곱하기 0.820000 은 2.574800 입니다.
}