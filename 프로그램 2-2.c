#include <stdio.h> // 우선 { } 의 역할은 중괄호 { } 안의 코드들을 묶어주는 역할을 한다.
void main() // void는 메모리를 차지하지 않기 때문에 void 타입의 변수는 성언이 불가, 중괄호 '{'로 시작하여 '}'로 끝난다.
{
	int			a, b, c; // a, b, c 모두 자료형을 정수로 지정한다는 뜻입니다

	a = 15; //정수 15을 a에 대입한다
	b = 20; //정수 20을 b에 대입한다
	c = a + b; //정수 a와 b을 더한 값을 c에 대입한다
	printf("%d 더하기 %d 입니다/n", a, b, c); // 15 더하기 20 은 35입니다.
}