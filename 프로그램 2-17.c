#include <stdio.h> // 우선 { } 의 역할은 중괄호 { } 안의 코드들을 묶어주는 역할을 한다.
void main() // void는 메모리를 차지하지 않기 때문에 void 타입의 변수는 성언이 불가, 중괄호 '{'로 시작하여 '}'로 끝난다.
{
	char	c;
	
	printf("문자를 하나 입력하세요/n"); // 문자를 하나 입력하세요
	scanf("%c", &c); // w(Enter),  "%c"는 "scanf" 함수에서 사용자의 문자 입력이 예상됨을 지정하는 데 사용됩니다. "%" 문자는 형식 지정자를 나타내는 데 사용되며 "c"는 문자 데이터 유형을 나타냅니다.

	printf("입력한 문자를 %c 입니다/n", c); // 입력한 문자는 w 입니다
}

