#include <stdio.h> // 우선 { } 의 역할은 중괄호 { } 안의 코드들을 묶어주는 역할을 한다.
void main() // void는 메모리를 차지하지 않기 때문에 void 타입의 변수는 성언이 불가, 중괄호 '{'로 시작하여 '}'로 끝난다.
{
	int  k1, k2; // 변수 "k1" 및 "k2"는 초기화되지 않습니다. 즉, 변수에 할당된 특정 값이 없습니다. 잠재적인 버그나 예기치 않은 동작을 방지하려면 변수를 코드에서 사용하기 전에 초기화하는 것이 중요합니다.

	printf("두개의 숫자를 입력하세요/n"); // 두개의 숫자를 입력하세요
	scanf("%d%d", &k1, &k2);				// 숫자를 입력 받아 변수 k1, k2 에 저장, // 25 (Enter), 37 (Enter)
	printf("입력하신 숫자는 %d 와 %d 입니다/n", k1, k2); // 입력하신 숫자는 25와 37 입니다
}

